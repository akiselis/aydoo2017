package ar.edu.untref.aydoo;

public class Mes {

	String mes;
	int idDeMes;

	public Mes(int idDeMes, String mes) {
		idDeMes = idDeMes;
		mes = mes;
	}

	public int getIdDeMes() {
		return idDeMes;
	}

	public void setIdDeMes(int idDeMes) {
		this.idDeMes = idDeMes;
	}

	public String getMes() {
		return mes;
	}

	public void setMes(String mes) {
		this.mes = mes;
	}

}
